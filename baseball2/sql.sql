/* CREAET TABLE */
CREATE TABLE MEMBERS (
	ID VARCHAR(20) NOT NULL,
	PW VARCHAR(20) DEFAULT NULL,
	NAME VARCHAR(20) DEFAULT NULL,
	AGE int DEFAULT NULL,
	FT VARCHAR(20) DEFAULT NULL,
	CONSTRAINT mem_id_pk PRIMARY KEY(ID)
)ENGINE=InnoDB DEFAULT CHARSET=utf8;

CREATE TABLE BOARD (
	NUM int AUTO_INCREMENT NOT NULL ,
	CNT int NOT NULL,
	WRITER VARCHAR(20) NOT NULL,
	TITLE VARCHAR(100) DEFAULT NULL,
	CONTENT VARCHAR(255) DEFAULT NULL,
	SEND_DATE TIMESTAMP DEFAULT NOW(),
	CONSTRAINT bd_n_pk PRIMARY KEY(NUM),
	FOREIGN KEY(WRITER) REFERENCES MEMBERS(ID)
)ENGINE=InnoDB DEFAULT CHARSET=utf8;

CREATE TABLE WEATHER (
	INDEX_NUM int NOT NULL,
	LOCATION VARCHAR(10),
	DAY1 TIMESTAMP DEFAULT NOW(),
	DAY VARCHAR(10),
	TEMP int,
	HUMIDITY int,
	RAINPROB int,
	SKY int,
	CONSTRAINT weather_day_pk PRIMARY KEY(INDEX_NUM)
)ENGINE=InnoDB DEFAULT CHARSET=utf8;

