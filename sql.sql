/* CREAET TABLE */
CREATE TABLE MEMBERS (
	ID CHAR(20) NOT NULL,
	PW CHAR(20) DEFAULT NULL,
	NAME CHAR(20) DEFAULT NULL,
	AGE int DEFAULT NULL,
	FT CHAR(20) DEFAULT NULL,
	CONSTRAINT mem_id_pk PRIMARY KEY(ID)
)ENGINE=InnoDB DEFAULT CHARSET=utf8;

CREATE TABLE BOARD (
	NUM NUMBER NOT NULL ,
	WRITER VARCHAR2(20) NOT NULL,
	TITLE VARCHAR2(100),
	CONTENT VARCHAR2(500),
	SEND_DATE DATE,
	CONSTRAINT bd_n_pk PRIMARY KEY(NUM),
	FOREIGN KEY(WRITER) REFERENCES MEMBERS(ID)
);

CREATE TABLE WEATHER (
	INDEX_NUM NUMBER NOT NULL,
	LOCATION VARCHAR2(10),
	DAY1 SYSDATE,
	DAY VARCHAR2(10),
	TEMP NUMBER,
	HUMIDITY NUMBER,
	RAINPROB NUMBER,
	SKY NUMBER,
	CONSTRAINT weather_day_pk PRIMARY KEY(INDEX_NUM)
);

